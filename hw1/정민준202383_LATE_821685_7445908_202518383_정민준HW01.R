plot(iris_setosa[, 3], iris_setosa[, 4],
     main = "Correlation of Petal Length and Petal Width",  # 제목
     xlab = "Length",                                       # x축 이름
     ylab = "Width",                                        # y축 이름
     xlim = c(0.5, 2),                                      # x축 범위
